package Day06_ArithmeticalOperators;

public class ArithmeticOperator {

    public static void main(String[] args) {
        System.out.println(10/4);
        System.out.println("10/4");//text
        System.out.println(12+3.0);
        System.out.println("12"+3);//concatenation
        System.out.println('a'+3);//character has a corresponding number
        System.out.println(12.0-4);//8.0
        double a = 12.0/3;
        System.out.println(a);
        int b = 10/3;
        System.out.println(b);//3
        double c = 10/3;
        System.out.println(c);//3.0

        int x = 3/2;
        System.out.println(x);
        double y = 3/2;
        System.out.println(y);

        double z = 3d/2;
        System.out.println(z);

        System.out.println(100%13);
        System.out.println(100%10);
        System.out.println(20%4.5);






    }
}
