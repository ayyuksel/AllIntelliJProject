package Day06_ArithmeticalOperators;

public class EscapeSequences_Recap {

    /*Escape sequences: for text
        \n: append new line
        \t: tab space, paragraph space
        \\: \
        \": double quote*/
    public static void main(String[] args) {
        System.out.println("\tHello Everyone\n\t\tHello Batch 21\n\t\t\tToday I new programming learned \"Java\"");

        System.out.println("My favorite quote is:\n\tWinter is coming\n\t\t\t\t-Ned Stark");
        System.out.println("\\/");

        System.out.println("Hi Cybertek\n\tToday is a great day!\nI am happy to be part of \"Bacth 21\".");

    }


}
