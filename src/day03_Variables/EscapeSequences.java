package day03_Variables;

public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Hello Everyone!\nMy name is Ayse, I am from Turkey");

        System.out.println("-------------------------------");


        System.out.println("\tToday I learned Java");
        System.out.println("it was so much fun");

        System.out.println("--------------------------------");

        System.out.println("/");
        System.out.println("\\");
        System.out.println("\'My name is Ayse\'");
        System.out.println("'My name is Ayse'");

        System.out.println("-----------------------------------");
        System.out.println("My favorite Tv series \"Game of Thrones\"");






    }
}
