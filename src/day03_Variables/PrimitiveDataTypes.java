package day03_Variables;

public class PrimitiveDataTypes {
    public static void main(String[] args) {
        //score is 85
      float score = 85;
        System.out.println(score);
        System.out.println("score");
        //compiler by default takes as int range
        //in order declare long upper or lower case l need be used
        long number1 = 99999999999999L;
        System.out.println(number1);
        double decimal1 = 0.5;
        //by default takes as double
        float flot1 = 0.5F;
        System.out.println(flot1);




    }
}
